using UnityEditor.VersionControl;
using UnityEngine;

public class testObject2 : MonoBehaviour, IInteractable
{
    [SerializeField] string message = "Press E to Kill Me";

    public string prompt => message;

    Renderer renderer;
    Material material;
    Color originalColour;

    private void Start()
    {
        renderer = GetComponent<Renderer>();
        material = renderer.material;
        originalColour = material.color;

    }

    public void Interact(Interactor interactor)
    {
        Destroy(gameObject);
    }

    public void OnHoverEnter(Interactor interactor)
    {
        material.color = Color.chartreuse;
    }

    public void OnHoverExit(Interactor interactor)
    {
        material.color= originalColour;
    }
}
